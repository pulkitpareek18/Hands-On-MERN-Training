// History of JavaScript

// JavaScript was created in 1995 by Brendan Eich while he was working at Netscape Communications Corporation. 
// It was initially developed in just 10 days and was originally named Mocha, then renamed to LiveScript, 
// and finally to JavaScript. The name "JavaScript" was chosen to capitalize on the popularity of Java at the time, 
// although the two languages are quite different.

// JavaScript was designed to be a lightweight, interpreted language that could be used to create dynamic and interactive 
// elements on web pages. It quickly gained popularity and became one of the core technologies of the World Wide Web, 
// alongside HTML and CSS.

// In 1996, Netscape submitted JavaScript to ECMA International for standardization, leading to the creation of 
// ECMAScript. The first edition of ECMAScript was published in 1997. Since then, several versions of ECMAScript 
// have been released, introducing new features and improvements to the language.

// Over the years, JavaScript has evolved significantly, with major updates such as ECMAScript 5 (2009) and 
// ECMAScript 6 (2015), which introduced features like classes, modules, arrow functions, and promises. 
// Today, JavaScript is a versatile language used not only for client-side web development but also for server-side 
// development (using Node.js), mobile app development, and even desktop applications. It remains one of the most 
// popular programming languages in the world.

